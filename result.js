
let mark = 75 ;

if( mark >= 80 ){
    console.log("Your Grade: " + "A+")

} else if( mark >= 70 && mark <= 79 ) {
    console.log("Your Grade: " + "A")

} else if( mark >= 60 && mark <= 69 ) {
    console.log("Your Grade: " + "B")

} else if( mark >= 50 && mark <= 59 ) {
    console.log("Your Grade: " + "C")

} else {
    console.log("Failed")
}

